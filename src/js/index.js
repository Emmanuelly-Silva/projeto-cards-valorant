let cards = [cardBrimstone, cardPhoenix, cardSage, cardSova, cardViper, cardCypher, cardReyna, cardKilljoy, cardBreach, cardOmen, cardJett, cardRaze, cardSkye, cardYoru, cardAstra, cardKayo, cardChamber, cardNeon, cardFade];
let selectedCard;
const btnForward = document.getElementById("btn-forward");
const btnBack = document.getElementById("btn-back");
let currentCard = 0;

function startCardList() {
    document.getElementById("start-card").style.display = "none";
    document.getElementById("valorant-slider").style.display = "flex";
    showCards(currentCard);
}

function showCards(cardIndex) {
    selectedCard = cards[cardIndex];

    let information = document.getElementById("information");
    let name = `<h2 class="name">${selectedCard.name}</h2>`;
    let role = `<div class = "information-role">
        <img src="${selectedCard.imageRole}" alt="${selectedCard.role}" class="image-role">
        <p class="role">${selectedCard.role}</p>
    </div>`;
    let audio = `<audio>
        <source src="${selectedCard.sound}" type="audio/mpeg">  
    </audio>`

    information.innerHTML = name + role + audio;

    let containerImage = document.getElementById("container-image");
    let image = `<img src="${selectedCard.image}" class="valorant-image" alt="${selectedCard.name}">`;
    containerImage.innerHTML = image;

    let description = document.getElementById("description");
    let titleDescription = `<h3 class="title">Descrição</h3>`
    let textDescription = `<p class="text" id="scroll-card">Origem: ${selectedCard.country}<br><br>${selectedCard.description}</p>`;
    description.innerHTML = titleDescription + textDescription;

    audio = document.querySelector('audio');
    audio.play();
}

btnForward.addEventListener("click", function () {
    if (currentCard === cards.length - 1) {
        currentCard = cards.length - (cards.length + 1);
    }

    currentCard++;
    showCards(currentCard);
});

btnBack.addEventListener("click", function () {
    if (currentCard === 0) {
        currentCard = cards.length;
    }

    currentCard--;
    showCards(currentCard);
});
